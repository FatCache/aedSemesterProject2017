    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblHospitalRequest.getModel();
        dtm.setRowCount(0);
        for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
            if(enterprise instanceof FarmerEnterprise){
                for(Organization organization:enterprise.getOrganizationDirectory().getOrganizationList()){
                    if(organization instanceof FarmerOrganization){
                            for(UserAccount farmerUserAccount : organization.getUserAccountDirectory().getUserAccountList()){
                                if(farmerUserAccount instanceof FarmerUserAccount){
                                    for(HospitalAssistanceRequest hosRequest: ((FarmerUserAccount) farmerUserAccount).getHosptialWorkQueue()){
                                        Object[] row = new Object[7];
                                        row[0] = farmerUserAccount.getEmployee().getName();
                                        row[1] = ((FarmerUserAccount) farmerUserAccount).getAddress();
                                        row[2] = hosRequest.getRequestDate();
                                        row[3] = hosRequest;
                                        row[4] = hosRequest.getStatus();
                                        Date date = hosRequest.getResolveDate();
                                        row[5] = date == null ? "Not Processed" : date;
                                        //UserAccount userTemp = hosRequest.getReceiver();
                                        //String resolvedBy = hosRequest.getReceiver().getEmployee().getName() == null? null:hosRequest.getReceiver().getEmployee().getName();
                                        row[6] = hosRequest.getReceiver();
                                        dtm.addRow(row);
                                    }
                                }
                            }
                    }
                }
            }
        }
    }

