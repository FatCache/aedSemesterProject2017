    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int seletectedRow = tblHospitalRequest.getSelectedRow();
        if(seletectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the Table", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            
            HospitalAssistanceRequest request = (HospitalAssistanceRequest) tblHospitalRequest.getValueAt(seletectedRow, 3);
            request.setReceiver(account);
            request.setStatus("Help Dispatched!");   
            request.setResolveDate(date);
            
            MarketNews marketNews = new MarketNews();
            marketNews.setDateAnnounced(Utility.getCurrentDate());
            marketNews.setNewsDetail("The hospital has read your plea and has dispatched help to your location");
            marketNews.setNewsSubject("Hospital Help Approval");
            
            ((FarmerUserAccount)request.getSender()).getMessageList().add(marketNews);
            
            JOptionPane.showMessageDialog(null, "Help Request Approved", "Success!", JOptionPane.OK_OPTION);


            
        }
        
        populateTable();
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

