    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        if(!textAreaDescription.getText().isEmpty() && Utility.intCheck(txtloanAmount.getText())){
        LoanRequest request = new LoanRequest();
        request.setLoanDescription(textAreaDescription.getText());
        request.setLoanAmount(Integer.parseInt(txtloanAmount.getText()));
        request.setRequestDate(Utility.getCurrentDate());
        request.setStatus("Pending");
        
        account.getFinanceWorkQueue().add(request);
        System.out.println("Loan Queue Added");
        
        populateTable();
        
        JOptionPane.showMessageDialog(null, "Loan Request Submitted", "Success!", JOptionPane.OK_OPTION);
        
        }else{
            JOptionPane.showMessageDialog(null, "Invalid Entry. Please Try Again", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_requestTestJButtonActionPerformed

