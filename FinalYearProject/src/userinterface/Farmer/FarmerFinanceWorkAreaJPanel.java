    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblLoanHistory.getModel();
        dtm.setRowCount(0);

        for (LoanRequest request : account.getFinanceWorkQueue()){
            Object[] row = new Object[6];
            row[0] = request.getLoanDescription();
            row[1] = request.getLoanAmount();
            row[2] = request.getRequestDate();
            row[3] = request.getStatus();
            row[4] = request.getReceiver();
            row[5] = request.getResolveDate();            
            dtm.addRow(row);
        }
    }
